Num_1 = int(input("Enter 1st number!: "))
Num_2 = int(input("Enter 2nd number!: "))

def mathematical_operations (Num_1 , Num_2):
    
    print(f"Addition of 1st and 2nd number is : {Num_1 + Num_2}")
    print(f"Subtraction of 1st and 2nd number is : {Num_1 - Num_2}")
    print(f"Multiplication of 1st and 2nd number is : {Num_1 * Num_2}")
    print(f"Division of 1st and 2nd number is : {Num_1 / Num_2}")
    
mathematical_operations(Num_1 , Num_2)